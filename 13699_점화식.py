from sys import stdin

n = int(stdin.readline())
t = [1, 1, 2]

if n < 3:
    print(t[n])
    exit()

for i in range(2, n):
    sum = 0
    for j in range(i+1):
        sum += t[j] * t[i-j]
    t.append(sum)

print(t[n])


